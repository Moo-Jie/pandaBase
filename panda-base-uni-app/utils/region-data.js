/**
 * 将树形结构的省市区数据转换为 multiSelector 需要的二维数组格式
 * @returns {Array} [省列表, 市列表, 区列表]
 */
export function getRegionColumns() {
	const provinces = regionData.map(p => p.label);
	const cities = regionData[0].children.map(c => c.label);
	const districts = regionData[0].children[0].children.map(d => d.label);
	
	return [provinces, cities, districts];
}

/**
 * 根据省索引获取该省的城市列表
 * @param {Number} provinceIndex 省索引
 * @returns {Array} 城市列表
 */
export function getCitiesByProvinceIndex(provinceIndex) {
	if (provinceIndex >= 0 && provinceIndex < regionData.length) {
		return regionData[provinceIndex].children.map(c => c.label);
	}
	return [];
}

/**
 * 根据省索引和市索引获取该市的区列表
 * @param {Number} provinceIndex 省索引
 * @param {Number} cityIndex 市索引
 * @returns {Array} 区列表
 */
export function getDistrictsByIndex(provinceIndex, cityIndex) {
	if (provinceIndex >= 0 && provinceIndex < regionData.length) {
		const province = regionData[provinceIndex];
		if (cityIndex >= 0 && cityIndex < province.children.length) {
			return province.children[cityIndex].children.map(d => d.label);
		}
	}
	return [];
}

/**
 * 根据索引数组获取省市区名称
 * @param {Array} indexes [省索引, 市索引, 区索引]
 * @returns {Array} [省名称, 市名称, 区名称]
 */
export function getRegionNamesByIndexes(indexes) {
	const [provinceIndex, cityIndex, districtIndex] = indexes;
	const names = [];
	
	if (provinceIndex >= 0 && provinceIndex < regionData.length) {
		const province = regionData[provinceIndex];
		names.push(province.label);
		
		if (cityIndex >= 0 && cityIndex < province.children.length) {
			const city = province.children[cityIndex];
			names.push(city.label);
			
			if (districtIndex >= 0 && districtIndex < city.children.length) {
				const district = city.children[cityIndex].children[districtIndex];
				names.push(district.label);
			}
		}
	}
	
	return names;
}

